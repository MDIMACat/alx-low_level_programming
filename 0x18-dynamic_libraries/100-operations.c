#include <stdio.h>

/**
 * add - adds to integers
 * a: integer parameter
 * b: integer parameter
 * Return: sum of two numbers
*/

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two integers
 * a: int parameter
 * b: int parameter
 * Return: Subtracted value of a and b
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two values
 * a: int parameter
 * b: int parameter
 * Return: multiplied value
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two values
 * a: int parameter
 * b: int parameter
 * Return: Divided value
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - gets remainder of two values
 * a: int parameter
 * b: int parameter
 * Return: remainder
*/

int mod(int a, int b)
{
	return (a % b);
}

